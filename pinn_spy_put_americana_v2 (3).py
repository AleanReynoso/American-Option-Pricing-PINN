"""## Sección 2 — Árbol Binomial CRR y Black-Scholes europeo (referencias)"""

def american_put_binomial(S, K, r, sigma, T, N=1000):
    dt = T/N; u = np.exp(sigma*np.sqrt(dt)); d = 1/u
    p = (np.exp(r*dt)-d)/(u-d); disc = np.exp(-r*dt)
    j = np.arange(N+1)
    V = np.maximum(K - S*(u**(N-2*j)), 0.0)
    for i in range(N-1,-1,-1):
        j = np.arange(i+1); S_i = S*(u**(i-2*j))
        V = np.maximum(disc*(p*V[:-1]+(1-p)*V[1:]), np.maximum(K-S_i,0))
    return float(V[0])

def binomial_boundary(S0, K, r, sigma, T, N=300):
    dt=T/N; u=np.exp(sigma*np.sqrt(dt)); d=1/u
    p=(np.exp(r*dt)-d)/(u-d); disc=np.exp(-r*dt)
    S_tree=np.zeros((N+1,N+1))
    for i in range(N+1):
        for j in range(i+1): S_tree[j,i]=S0*(u**(i-j))*(d**j)
    V_tree=np.zeros((N+1,N+1))
    for j in range(N+1): V_tree[j,N]=max(K-S_tree[j,N],0)
    B=np.zeros(N+1); B[N]=K
    for i in range(N-1,-1,-1):
        B_i=0
        for j in range(i+1):
            hold=disc*(p*V_tree[j,i+1]+(1-p)*V_tree[j+1,i+1])
            intr=max(K-S_tree[j,i],0)
            V_tree[j,i]=max(hold,intr)
            if intr>0 and V_tree[j,i]<=intr+1e-8 and S_tree[j,i]>B_i:
                B_i=S_tree[j,i]
        B[i]=B_i if B_i>0 else B[i+1]
    return np.linspace(0,T,N+1), B

def bs_european_put(S, K, r, sigma, tau):
    tau = np.maximum(tau, 1e-8)
    d1  = (np.log(S/K)+(r+0.5*sigma**2)*tau)/(sigma*np.sqrt(tau))
    return K*np.exp(-r*tau)*norm.cdf(-(d1-sigma*np.sqrt(tau)))-S*norm.cdf(-d1)


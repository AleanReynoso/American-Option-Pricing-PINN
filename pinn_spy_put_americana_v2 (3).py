"""## Sección 3 — Arquitectura de la PINN

**Correcciones clave respecto a implementaciones naive:**
- Salida lineal en `PriceNet` (sin `Softplus`) → gradientes correctos para Neumann
- `BoundNet` inicializada con sesgo positivo → $B \approx K$ al inicio
- `detach()` de $B$ antes del gradiente Neumann → $\partial_S V$ correcto
"""

S_SCALE = float(K); T_SCALE = float(T)

class PriceNet(nn.Module):
    def __init__(self, hidden=64, n_layers=4):
        super().__init__()
        layers = [nn.Linear(2,hidden), nn.Tanh()]
        for _ in range(n_layers-1): layers += [nn.Linear(hidden,hidden), nn.Tanh()]
        layers += [nn.Linear(hidden,1)]
        self.net = nn.Sequential(*layers)
        for m in self.modules():
            if isinstance(m,nn.Linear): nn.init.xavier_normal_(m.weight); nn.init.zeros_(m.bias)

    def forward(self, S, t):
        return self.net(torch.cat([S/S_SCALE, t/T_SCALE], dim=1)) * S_SCALE

class BoundNet(nn.Module):
    def __init__(self, K_val, hidden=32, n_layers=3):
        super().__init__()
        self.K_val = K_val
        layers = [nn.Linear(1,hidden), nn.Tanh()]
        for _ in range(n_layers-1): layers += [nn.Linear(hidden,hidden), nn.Tanh()]
        layers += [nn.Linear(hidden,1)]
        self.net = nn.Sequential(*layers)
        for m in self.modules():
            if isinstance(m,nn.Linear): nn.init.xavier_normal_(m.weight); nn.init.zeros_(m.bias)
        self.net[-1].bias.data.fill_(0.0)   # B ≈ K al inicio

    def forward(self, t):
        return self.K_val * (0.7 + 0.3*torch.sigmoid(self.net(t/T_SCALE)))

